CREATE OR REPLACE FUNCTION public.current_user_role()
RETURNS public.user_role
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
  SELECT u.role
  FROM public.users u
  WHERE u.id = auth.uid()
$$;

CREATE OR REPLACE FUNCTION public.current_user_factory_id()
RETURNS TEXT
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
  SELECT u.factory_id
  FROM public.users u
  WHERE u.id = auth.uid()
$$;

CREATE OR REPLACE FUNCTION public.has_feature_permission(
  p_feature_key TEXT,
  p_factory_id TEXT DEFAULT NULL
)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
  SELECT COALESCE((
    SELECT
      u.role = 'admin'::public.user_role
      OR (
        u.factory_id = COALESCE(p_factory_id, u.factory_id)
        AND EXISTS (
          SELECT 1
          FROM public.role_feature_permissions rfp
          JOIN public.app_features af ON af.key = rfp.feature_key AND af.is_active
          WHERE rfp.factory_id = u.factory_id
            AND rfp.role = u.role
            AND rfp.feature_key = p_feature_key
            AND rfp.allowed
        )
      )
    FROM public.users u
    WHERE u.id = auth.uid()
  ), false)
$$;

CREATE OR REPLACE FUNCTION public.has_any_feature_permission(
  p_feature_keys TEXT[],
  p_factory_id TEXT DEFAULT NULL
)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM unnest(p_feature_keys) AS feature_key
    WHERE public.has_feature_permission(feature_key, p_factory_id)
  )
$$;

CREATE OR REPLACE FUNCTION public.can_access_factory(p_factory_id TEXT)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
  SELECT COALESCE((
    SELECT u.role = 'admin'::public.user_role OR u.factory_id = p_factory_id
    FROM public.users u
    WHERE u.id = auth.uid()
  ), false)
$$;

CREATE OR REPLACE FUNCTION public.get_my_permissions()
RETURNS TABLE(feature_key TEXT)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
  SELECT af.key
  FROM public.app_features af
  JOIN public.users u ON u.id = auth.uid()
  LEFT JOIN public.role_feature_permissions rfp
    ON rfp.factory_id = u.factory_id
   AND rfp.role = u.role
   AND rfp.feature_key = af.key
  WHERE af.is_active
    AND (u.role = 'admin'::public.user_role OR COALESCE(rfp.allowed, false))
  ORDER BY af.sort_order
$$;

CREATE OR REPLACE FUNCTION public.get_role_permissions(p_factory_id TEXT)
RETURNS TABLE(
  factory_id TEXT,
  role public.user_role,
  feature_key TEXT,
  allowed BOOLEAN,
  updated_at TIMESTAMPTZ,
  updated_by UUID
)
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
BEGIN
  IF auth.uid() IS NULL OR NOT (
    public.current_user_role() = 'admin'::public.user_role
    OR (
      public.current_user_factory_id() = p_factory_id
      AND public.has_feature_permission('userManagement', p_factory_id)
    )
  ) THEN
    RAISE EXCEPTION 'permission denied' USING ERRCODE = '42501';
  END IF;

  RETURN QUERY
  SELECT
    rfp.factory_id,
    rfp.role,
    rfp.feature_key,
    CASE WHEN rfp.role = 'admin'::public.user_role THEN true ELSE rfp.allowed END,
    rfp.updated_at,
    rfp.updated_by
  FROM public.role_feature_permissions rfp
  JOIN public.app_features af ON af.key = rfp.feature_key AND af.is_active
  WHERE rfp.factory_id = p_factory_id
  ORDER BY af.sort_order, rfp.role;
END
$$;

-- Operational screens need names and roles, but must not receive account email
-- addresses or other profile fields. Keep this projection behind a bounded RPC.
CREATE OR REPLACE FUNCTION public.get_user_directory()
RETURNS TABLE(
  id UUID,
  name TEXT,
  role public.user_role,
  factory_id TEXT
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
  SELECT u.id, u.name, u.role, u.factory_id
  FROM public.users u
  JOIN public.users caller ON caller.id = auth.uid()
  WHERE auth.uid() IS NOT NULL
    AND (
      caller.role = 'admin'::public.user_role
      OR (
        u.factory_id = caller.factory_id
        AND public.has_any_feature_permission(
          ARRAY['inspection','defects','analytics','reports','aiInsights','userManagement'],
          caller.factory_id
        )
      )
    )
  ORDER BY u.name, u.id
$$;

CREATE OR REPLACE FUNCTION public.set_role_permissions(
  p_factory_id TEXT,
  p_changes JSONB
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
DECLARE
  change_item JSONB;
  target_role public.user_role;
  target_feature TEXT;
  target_allowed BOOLEAN;
  previous_allowed BOOLEAN;
BEGIN
  IF auth.uid() IS NULL OR public.current_user_role() <> 'admin'::public.user_role THEN
    RAISE EXCEPTION 'only administrators can change permissions' USING ERRCODE = '42501';
  END IF;

  IF NOT EXISTS (SELECT 1 FROM public.factories f WHERE f.id = p_factory_id) THEN
    RAISE EXCEPTION 'unknown factory: %', p_factory_id USING ERRCODE = '22023';
  END IF;

  IF jsonb_typeof(p_changes) <> 'array' THEN
    RAISE EXCEPTION 'p_changes must be a JSON array' USING ERRCODE = '22023';
  END IF;

  FOR change_item IN SELECT value FROM jsonb_array_elements(p_changes)
  LOOP
    previous_allowed := NULL;

    BEGIN
      target_role := (change_item ->> 'role')::public.user_role;
      target_feature := change_item ->> 'feature_key';
      target_allowed := (change_item ->> 'allowed')::BOOLEAN;
    EXCEPTION WHEN OTHERS THEN
      RAISE EXCEPTION 'invalid permission change: %', change_item USING ERRCODE = '22023';
    END;

    IF target_role = 'admin'::public.user_role THEN
      RAISE EXCEPTION 'administrator permissions are immutable' USING ERRCODE = '22023';
    END IF;

    IF target_role IS NULL OR target_allowed IS NULL THEN
      RAISE EXCEPTION 'role and allowed are required: %', change_item USING ERRCODE = '22023';
    END IF;

    IF target_feature IS NULL OR NOT EXISTS (
      SELECT 1 FROM public.app_features af WHERE af.key = target_feature AND af.is_active
    ) THEN
      RAISE EXCEPTION 'unknown feature: %', target_feature USING ERRCODE = '22023';
    END IF;

    SELECT rfp.allowed
    INTO previous_allowed
    FROM public.role_feature_permissions rfp
    WHERE rfp.factory_id = p_factory_id
      AND rfp.role = target_role
      AND rfp.feature_key = target_feature
    FOR UPDATE;

    IF previous_allowed IS DISTINCT FROM target_allowed THEN
      INSERT INTO public.role_feature_permissions (
        factory_id, role, feature_key, allowed, updated_at, updated_by
      ) VALUES (
        p_factory_id, target_role, target_feature, target_allowed, now(), auth.uid()
      )
      ON CONFLICT (factory_id, role, feature_key) DO UPDATE SET
        allowed = EXCLUDED.allowed,
        updated_at = EXCLUDED.updated_at,
        updated_by = EXCLUDED.updated_by;

      INSERT INTO public.permission_audit (
        factory_id, role, feature_key, old_allowed, new_allowed, changed_by
      ) VALUES (
        p_factory_id, target_role, target_feature, previous_allowed, target_allowed, auth.uid()
      );
    END IF;
  END LOOP;
END
$$;

-- Public TV monitor receives only the bounded, non-user fields required by its UI.
-- Base tables remain unavailable to anon.
CREATE OR REPLACE FUNCTION public.get_public_monitor_data(
  p_factory_id TEXT,
  p_start_at TIMESTAMPTZ,
  p_end_at TIMESTAMPTZ
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
BEGIN
  IF p_factory_id IS NULL OR NOT EXISTS (
    SELECT 1 FROM public.factories f
    WHERE f.id = p_factory_id AND f.is_active = true
  ) THEN
    RAISE EXCEPTION 'invalid or inactive factory' USING ERRCODE = '22023';
  END IF;

  IF p_start_at IS NULL OR p_end_at IS NULL
     OR p_end_at <= p_start_at
     OR p_end_at > p_start_at + interval '32 days'
     OR p_start_at < now() - interval '32 days'
     OR p_start_at > now() + interval '1 day' THEN
    RAISE EXCEPTION 'invalid monitor date range' USING ERRCODE = '22023';
  END IF;

  RETURN jsonb_build_object(
    'defects', COALESCE((
      SELECT jsonb_agg(jsonb_build_object(
        'id', d.id,
        'created_at', d.created_at,
        'inspection_id', d.inspection_id,
        'model_id', d.model_id,
        'defect_type', d.defect_type
      ) ORDER BY d.created_at DESC)
      FROM public.defects d
      WHERE d.factory_id = p_factory_id
        AND d.created_at >= p_start_at
        AND d.created_at <= p_end_at
    ), '[]'::jsonb),
    'inspections', COALESCE((
      SELECT jsonb_agg(jsonb_build_object('id', i.id, 'machine_id', i.machine_id))
      FROM public.inspections i
      WHERE i.factory_id = p_factory_id
        AND EXISTS (
          SELECT 1 FROM public.defects d
          WHERE d.inspection_id = i.id
            AND d.factory_id = p_factory_id
            AND d.created_at >= p_start_at
            AND d.created_at <= p_end_at
        )
    ), '[]'::jsonb),
    'machines', COALESCE((
      SELECT jsonb_agg(jsonb_build_object('id', m.id, 'name', m.name) ORDER BY m.name)
      FROM public.machines m
      WHERE m.factory_id = p_factory_id
        AND m.status = 'active'
        AND EXISTS (
          SELECT 1
          FROM public.inspections i
          JOIN public.defects d ON d.inspection_id = i.id
          WHERE i.machine_id = m.id
            AND i.factory_id = p_factory_id
            AND d.factory_id = p_factory_id
            AND d.created_at >= p_start_at
            AND d.created_at <= p_end_at
        )
    ), '[]'::jsonb),
    'product_models', COALESCE((
      SELECT jsonb_agg(jsonb_build_object('id', pm.id, 'code', pm.code) ORDER BY pm.code)
      FROM public.product_models pm
      WHERE EXISTS (
        SELECT 1 FROM public.defects d
        WHERE d.model_id = pm.id
          AND d.factory_id = p_factory_id
          AND d.created_at >= p_start_at
          AND d.created_at <= p_end_at
      )
    ), '[]'::jsonb),
    'defect_types', COALESCE((
      SELECT jsonb_agg(jsonb_build_object('id', dt.id, 'code', dt.code, 'name', dt.name) ORDER BY dt.code)
      FROM public.defect_types dt
      WHERE dt.is_active = true
        AND EXISTS (
          SELECT 1 FROM public.defects d
          WHERE (d.defect_type = dt.id::text OR d.defect_type = dt.code)
            AND d.factory_id = p_factory_id
            AND d.created_at >= p_start_at
            AND d.created_at <= p_end_at
        )
    ), '[]'::jsonb)
  );
END
$$;

