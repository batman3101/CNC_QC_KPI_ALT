-- Supabase-backed feature permissions and RLS hardening.
-- The permission matrix is scoped per factory. Admin is the immutable break-glass role.

CREATE TABLE IF NOT EXISTS public.app_features (
  key TEXT PRIMARY KEY,
  label_key TEXT NOT NULL,
  route TEXT NOT NULL UNIQUE,
  sort_order INTEGER NOT NULL UNIQUE,
  is_active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT app_features_key_format CHECK (key ~ '^[a-z][A-Za-z]+$')
);

CREATE TABLE IF NOT EXISTS public.role_feature_permissions (
  factory_id TEXT NOT NULL REFERENCES public.factories(id) ON UPDATE CASCADE ON DELETE CASCADE,
  role public.user_role NOT NULL,
  feature_key TEXT NOT NULL REFERENCES public.app_features(key) ON UPDATE CASCADE ON DELETE CASCADE,
  allowed BOOLEAN NOT NULL DEFAULT false,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_by UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  PRIMARY KEY (factory_id, role, feature_key)
);

CREATE INDEX IF NOT EXISTS idx_role_feature_permissions_lookup
  ON public.role_feature_permissions (factory_id, role, feature_key)
  INCLUDE (allowed);

CREATE TABLE IF NOT EXISTS public.permission_audit (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  factory_id TEXT NOT NULL REFERENCES public.factories(id) ON UPDATE CASCADE ON DELETE RESTRICT,
  role public.user_role NOT NULL,
  feature_key TEXT NOT NULL REFERENCES public.app_features(key) ON UPDATE CASCADE ON DELETE RESTRICT,
  old_allowed BOOLEAN,
  new_allowed BOOLEAN NOT NULL,
  changed_by UUID NOT NULL REFERENCES auth.users(id) ON DELETE RESTRICT,
  changed_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_permission_audit_factory_changed_at
  ON public.permission_audit (factory_id, changed_at DESC);

