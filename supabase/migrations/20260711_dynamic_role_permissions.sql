-- Remove every existing permissive policy on application tables before recreating them.
DO $$
DECLARE
  policy_record RECORD;
BEGIN
  FOR policy_record IN
    SELECT schemaname, tablename, policyname
    FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = ANY (ARRAY[
        'factories', 'machines', 'inspections', 'inspection_results', 'defects',
        'product_models', 'inspection_items', 'inspection_processes', 'defect_types',
        'spc_alerts', 'users'
      ])
  LOOP
    EXECUTE format(
      'DROP POLICY IF EXISTS %I ON %I.%I',
      policy_record.policyname,
      policy_record.schemaname,
      policy_record.tablename
    );
  END LOOP;
END
$$;

